class about_us: UIViewController {

    @IBOutlet weak var txt_view:UITextView!
    
    @IBOutlet weak var view_navigation_bar:UIView! {
        didSet {
            view_navigation_bar.applyGradient()
        }
    }
    
    @IBOutlet weak var btn_back:UIButton! {
        didSet {
            btn_back.tintColor = .white
        }
    }
    
    @IBOutlet weak var view_navigation_title:UILabel! {
        didSet {
            if let language = UserDefaults.standard.string(forKey: str_language_convert) {
                print(language as Any)
                
                if (language == "en") {
                    view_navigation_title.text = "About Zarib"
                } else {
                    view_navigation_title.text = "যারিব সম্পর্কে জানুন"
                }
                
            }
            view_navigation_title.textColor = .white
        }
    }
    
    override func viewDidLoad() {
        super.viewDidLoad()
        
        self.sideBarMenuClick()
        // self.view.addGestureRecognizer(self.revealViewController().panGestureRecognizer())
        
        self.validation_before_accept_booking()
    }

    @objc func sideBarMenuClick() {
        
        if revealViewController() != nil {
            
            btn_back.addTarget(self.revealViewController(), action: #selector(SWRevealViewController.revealToggle(_:)), for: .touchUpInside)
            
            revealViewController().rearViewRevealWidth = 300
            view.addGestureRecognizer(self.revealViewController().panGestureRecognizer())
            
        }
    }
    
    @objc func validation_before_accept_booking() {
        self.set_location_hour_WB(str_show_loader: "yes")
    }
    
